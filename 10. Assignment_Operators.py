
# Assignment Operators ( = , += , -= , *= , /= , %= , **= )

num = 10
num = num + 10                   #10+10 answer 20 its very long 
print ("num :", num)

num = 10
num += 10                        # answer 20
print ("num :", num)

num = 10
num -= 10                        # answer 0
print ("num :", num)

num = 10
num *= 5                         # answer 50
print ("num :", num)

num = 10
num /= 5                         # answer 2.0
print ("num :", num)

num = 10
num %= 5                         # answer 0
print ("num :", num)

num = 10
num **= 5                        # answer 100000
print ("num :", num)